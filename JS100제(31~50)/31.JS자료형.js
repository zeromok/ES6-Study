/*
    문제 31. : 자바스크립트 자료형의 복잡도

    다음 배열 내장함수의 시간 복잡도가 O(1)이 아닌 것을 모두 고르시오.

    1)  arr[i]
    2)  arr.push(5)
    3)  arr.slice()
    4)  arr.pop()
    5)  arr.includes(5)

    답 : 3, 5

    풀이 : 한단계로는 끝나지 않는 내장함수를 고르면 된다.
    시간복잡도 = O(1), O(n)
    O(1) : 단계수 1 -> 한단계
    O(n) : 단계수 n -> n단계
*/