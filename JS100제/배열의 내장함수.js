/*
    문제2: 배열의 내장함수
    <pass>부분에 배열 내장함수를 이용하여 코드를 입력하고 다음과 같이 출력되게 하세요.

    데이터
    var arr = [200, 100, 300];
    <pass>
    console.log(arr);

    출력
    [200, 100, 10000, 300]
*/

// 방법 1.
let arr = [200, 100, 300];
arr[2] = 10000;
arr[3] = 300;
console.log(arr);

// 방법 2. splice(); 사용
// splice( 추가 or 제거할 배열요소 인덱스 설정, 제거 할 요소 개수 설정, 추가할 요소...(여러개 가능));
let arr00 = [200, 100, 300];
arr00.splice(2, 0, 10000);
console.log(arr00);
